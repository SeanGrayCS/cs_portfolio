public class Gray_Variables
{
	public static void main( String[] args )
	{
		
		//integer variables
		byte byteOne = 127;
		short shortOne = -23948;
		int intOne = 19232;
		long longOne = 474332243;

		//decimal variables
		float floatOne = 27.3221f;
		double doubleOne = 22.23;

		//other integer types
		char charOne = 'Z';

		//other types
		String stringOne = "Hello World!";
		boolean boolOne = true;

		//output your information here
		System.out.println("/////////////////////////////////");
		System.out.println("*                               *");
		System.out.println("*        integer types          *");
		System.out.println("*                               *");
		System.out.println("*8 bit - byteOne = "+byteOne+"\t\t*");
		System.out.println("*16 bit - shortOne = "+shortOne+"\t*");
		System.out.println("*32 bit - intOne = "+intOne+"\t*");
		System.out.println("*64 bit - longOne = "+longOne+"\t*");
		System.out.println("*                               *");
		System.out.println("*           real types          *");
		System.out.println("*                               *");
		System.out.println("*32 bit - floatOne = "+floatOne+"\t*");
		System.out.println("*64 bit - doubleOne = "+doubleOne+"\t*");
		System.out.println("*                               *");
		System.out.println("*      other integer types      *");
		System.out.println("*                               *");
		System.out.println("*16 bit - charOne = "+charOne+"\t\t*");
		System.out.println("*                               *");
		System.out.println("*           other types         *");
		System.out.println("*                               *");
		System.out.println("*booleanOne = "+boolOne+"\t\t*");
		System.out.println("*stringOne = "+stringOne+"\t*");
		System.out.println("\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\");

	}
}